
public class AnotacaoAula {
	/*
	 * PILARES POO:
	 * 
	 * - É um paradigma de programação baseado no conceito de "objetos", que podem
	 * conter dados na forma de campos, também conhecidos como atributos, e códigos,
	 * na forma de procedimentos.
	 * 
	 * - Análogo ao mundo real: Conta bancária, Aluno, Cliente e etc.
	 * 
	 * - Reutilização de código, capacidade da funcionalidade do programa mais
	 * próximo do mundo real.
	 * 
	 * ENCAPSULAMENTO: Nem tudo que eu faço precisa ser público. O encapsulamento
	 * refere-se à prática de esconder os detalhes internos de uma classe dos
	 * objetos externos. Isso é alcançado por meio do uso de modificadores de
	 * acesso, como público, privado e protegido, para controlar o acesso aos
	 * atributos e métodos de uma classe. Ao encapsular, você protege os dados
	 * sensíveis de modificações acidentais ou não autorizadas e promove uma melhor
	 * organização do código.
	 * 
	 * HERANÇA: Características e comportamentos comuns podem ser elevados e
	 * compartilhados através de uma hierarquia de objetos. A herança permite que
	 * uma classe (subclasse ou classe derivada) herde atributos e métodos de outra
	 * classe (superclasse ou classe base). Isso promove a reutilização de código,
	 * pois você pode criar uma nova classe baseada em uma classe existente,
	 * estendendo ou sobrescrevendo seu comportamento conforme necessário. A herança
	 * ajuda a criar uma hierarquia de classes, onde classes mais específicas herdam
	 * características de classes mais gerais.
	 * 
	 * ABSTRAÇÃO: É a disponibilidade para determinar a lógica de um ou vários
	 * comportamentos em um objeto. Abstração envolve a representação de objetos do
	 * mundo real no programa, focando nos aspectos relevantes e ignorando os
	 * detalhes irrelevantes. Ela permite que você modele entidades complexas como
	 * classes, que contêm características (atributos) e comportamentos (métodos). A
	 * abstração ajuda a criar uma separação clara entre a interface pública (como
	 * os métodos públicos) e a implementação interna de uma classe.
	 * 
	 * POLIMORFISMO: São as inúmeras maneiras de se realizar uma mesma ação.
	 * Exemplo: Veículo determina duas ações como acelerar e frear, primeiramente
	 * precisamos identificar se estaremos nos referindo a Carro ou Motocicleta para
	 * determinar a lógica de aceleração e frenagem dos respectivos veículos.
	 * Polimorfismo refere-se à capacidade de objetos de diferentes classes
	 * responderem ao mesmo método de maneira específica a sua própria classe. Isso
	 * permite tratar objetos de diferentes classes de forma uniforme através de uma
	 * interface comum. O polimorfismo pode ser alcançado por meio de herança e
	 * interfaces. Ele promove a flexibilidade, permitindo que você escreva código
	 * genérico que funcione com vários tipos de objetos.
	 * 
	 * 
	 */
}
