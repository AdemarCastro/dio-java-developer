public class Classes {
  
  /* 
   * Classes: São um molde (caracteristicas e ações que essa abstração poderá realizar), templates onde serão criados nossos objetos. Objetos nada mais são que instâncias dessas classes.
   * 
   * Java Beans: Getters e Setters: Utilizados para inserir e retornar os valores armazenados nos objetos das classes criadas
   * 
   * Convenções:
   * 
   * - Classe de Modelo (Model): Classes que representam estrutura de domínio da aplicação, exemplo: Cliente, Pedido, NotaFiscal e etc. Elas são utilzadas para criar a planta da abstração.
   * - Classe de Serviço (Service): Classes que contém regras de negócio e validação de nosso sistema.
   * - Classe de Repositório (Repository): Classes que contém uma integração com o banco de dados.
   * - Classe de Controle (Controller): Classes que possuem a finalidade de disponibilizar alguma comunicação externa à nossa aplicação, tipo http web ou webservices.
   * - Classe Utilitária (Util): Classe que contém recursos comuns à toda nossa aplicação.
   * 
   * Exemplo de Sistema feito com POO:
   * 
   * ATORES (Model)
   * - Empresa.java
   * - Veiculo.java
   * - Cliente.java
   * - NotaFiscal.java
   * - Colaborador.java
   * - Peca.java
   * 
   * SERVIÇOS (Service)
   * - VeiculoService.java
   * - ClienteService.java
   * - OficinaService.java
   * - NotaFiscalService.java
   * 
   * FERRAMENTAS (Utils)
   * - FormatadorUtil.java
   * - ValidadorUtil.java
   * - Calculadora.java
   * - ChaveInglesa.java
   * 
   * REPOSITÓRIOS (Repository)
   * - VeiculoRepository.java
   * - ClienteRepository.java
   * - PecaRepository.java
   * 
   */

}
